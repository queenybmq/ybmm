s = "hello"
#s[0]表示s里的第一个字符
print(s[0])
#s[4]表示s里的第五个字符
print(s[4])
#s[-1]表示s里倒数第一个字符
print(s[-1])
#s[0:3]表示s里第一个字符到第三个字符
print(s[0:3])
#s[0:4:2]表示s里第一个到第三个的偶数字符也就是0和2，所以是第一个字符和第三个字符
print(s[0:4:2])
#s[:]表示s里第一个字符到最后一个字符
print(s[:])
#s[:3]表示s里第一个字符到第三个字符
print(s[:3])
#s[::-1]表示倒数第一个字符到最后一个字符，也就是最后一个字符到第一个字符
print(s[::-1])
#s[1:]表示s里第二个字符到最后一个字符
print(s[1:])

